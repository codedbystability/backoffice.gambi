const themeReducer = (state = AuthenticationStates, action) => {
    switch (action.type) {
        case THEME_CONSTANTS.SET_THEME:
            return {
                ...state,
                // theme: state.theme === "light" ? "dark" : "light",
                theme: action.data,
            };

        case THEME_CONSTANTS.SET_VENDOR:
            return {
                ...state,
                vendor: action.data
            }

        default:
            return state;

    }
};

export default themeReducer;
